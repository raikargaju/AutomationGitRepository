package assignmentMethods;
class Demof
{
	Demof()
	{
		int cube = 0;
		for(int i = 1;i <= 10;i++)
		{
			cube  = i * i * i;
			System.out.println(cube);
		}
	}
}
public class Cube10NumsReverse {

	public static void main(String[] args) {
		// Cube first 10 Numbers in Reverse order.
		new Demof();
	}
}