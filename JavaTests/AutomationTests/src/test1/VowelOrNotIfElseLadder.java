package test1;

public class VowelOrNotIfElseLadder {

	public static void main(String[] args) {
		// Vowel or Not using if else Ladder.
		char ch = 'e';
		if(ch=='a')
		{
			System.out.println(ch+" is a vowel");
		}
		else if(ch=='e')
		{
			System.out.println(ch+" is a vowel");
		}
		else if(ch=='i')
		{
			System.out.println(ch+" is a vowel");
		}
		else if(ch=='o')
		{
			System.out.println(ch+" is a vowel");
		}
		else if(ch=='u')
		{
			System.out.println(ch+" is a vowel");
		}
		else
		{
			System.out.println(ch+" is not a vowel");
		}
	}
}